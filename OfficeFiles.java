public class OfficeFiles {
    int count;
    String filecontent;
    public OfficeFiles(int count,String filecontent)
    {
        this.count=count;
        this.filecontent=filecontent;
    }
    public int getCount()
    {
        return count;
    }
    public String getFilecontent()
    {
        return filecontent;
    }
}
